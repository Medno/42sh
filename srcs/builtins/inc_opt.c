#include "sh.h"

int		inc_opt(char **arg)
{
	int		i;

	i = 1;
	while (arg && arg[i])
	{
		if (ft_strequ(arg[i], "--"))
			return (i + 1);
		if (arg[i][0] == '-')
			i++;
		else
			return (i);
	}
	return (i);
}
