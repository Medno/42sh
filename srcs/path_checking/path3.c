#include "sh.h"

int	go_to_last(char *str, char c)
{
	int i;

	i = 0;
	while (str[i])
		i++;
	i--;
	while (i >= 0 && str[i] && str[i] != c)
		i--;
	if (i == -1)
		i++;
	return (i);
}
