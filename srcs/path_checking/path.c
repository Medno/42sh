#include "sh.h"

char	*paste_path(char *p1, char *p2)
{
	int		len1;
	char	*new;

	len1 = ft_strlen(p1);
	if (p1[len1 - 1] == '/' && p2[0] == '/')
		p1[len1 - 1] = 0;
	else if (p1[len1 - 1] != '/' && p2[0] != '/')
		p1 = ft_strjoindel(p1, "/");
	new = ft_strjoin(p1, p2);
	return (new);
}
