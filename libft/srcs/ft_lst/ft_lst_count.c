#include "libft.h"

int		ft_lst_count(t_list **alst)
{
	int		i;
	t_list	*tmp;

	tmp = *alst;
	i = 0;
	while (tmp)
	{
		tmp = tmp->next;
		i++;
	}
	return (i);
}
